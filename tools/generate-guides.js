// Generate rich tour-guide content (Hebrew) for every stop in the trip.
//
// For each stop we call Claude Sonnet 4.6 with prompt caching on a long Hebrew
// system prompt that defines the tour-guide voice, output schema, and tone.
// Output is written incrementally to js/guides.js so a crash mid-run doesn't
// lose work — re-runs skip stops already generated.
//
// Usage:
//   node tools/generate-guides.js              (run all missing stops)
//   node tools/generate-guides.js --day 2      (only Day 2)
//   node tools/generate-guides.js --stop d2-s4 (only that stop)
//   node tools/generate-guides.js --redo d2-s4 (regenerate one stop)
//   node tools/generate-guides.js --limit 5    (cap stops per run, for testing)

const fs = require('fs');
const path = require('path');
const Anthropic = require('@anthropic-ai/sdk').default;

// --- env ---
const envPath = path.join(__dirname, '..', '.env');
if (fs.existsSync(envPath)) {
  for (const line of fs.readFileSync(envPath, 'utf8').split(/\r?\n/)) {
    const m = line.match(/^\s*([A-Z_]+)\s*=\s*(.+?)\s*$/);
    if (m) process.env[m[1]] = m[2];
  }
}
if (!process.env.ANTHROPIC_API_KEY) {
  console.error('Missing ANTHROPIC_API_KEY (looked in .env and process env)');
  process.exit(1);
}

const client = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY });
const MODEL = 'claude-sonnet-4-6';

// --- args ---
const argv = process.argv.slice(2);
function arg(flag) {
  const i = argv.indexOf(flag);
  return i >= 0 ? argv[i + 1] : null;
}
const ONLY_DAY = arg('--day') ? parseInt(arg('--day'), 10) : null;
const ONLY_STOP = arg('--stop');
const REDO_STOP = arg('--redo');
const LIMIT = arg('--limit') ? parseInt(arg('--limit'), 10) : Infinity;

// --- data ---
const TRIP = require(path.join(__dirname, '..', 'js', 'data.js'));

const GUIDES_PATH = path.join(__dirname, '..', 'js', 'guides.js');
function loadExistingGuides() {
  if (!fs.existsSync(GUIDES_PATH)) return {};
  const src = fs.readFileSync(GUIDES_PATH, 'utf8');
  const m = src.match(/const TRIP_GUIDES\s*=\s*(\{[\s\S]*?\});\s*\n\s*if/);
  if (!m) return {};
  try { return JSON.parse(m[1]); } catch (e) { return {}; }
}
function saveGuides(guides) {
  const out =
    `// AUTO-GENERATED by tools/generate-guides.js — do not hand-edit.\n` +
    `// Per-stop tour-guide content in Hebrew: intro, history, geology, lookFor[], momTip.\n\n` +
    `const TRIP_GUIDES = ${JSON.stringify(guides, null, 2)};\n\n` +
    `if (typeof module !== 'undefined') module.exports = TRIP_GUIDES;\n`;
  fs.writeFileSync(GUIDES_PATH, out);
}

const guides = loadExistingGuides();

// --- prompt ---
