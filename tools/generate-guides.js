// --- per-stop context ---
function buildUserPrompt(day, stop) {
  const dayCtx = `יום ${day.dayNumber} (${day.date}) — ${day.title}${day.titleHe ? ` (${day.titleHe})` : ''}`;
  const region = day.region ? `\nאזור: ${day.region}` : '';
  const hotel = day.hotel ? `\nמלון לאותו לילה: ${day.hotel.name}` : '';
  const coords = stop.coordinates ? `\nקואורדינטות: ${stop.coordinates.lat.toFixed(5)}, ${stop.coordinates.lng.toFixed(5)}` : '';
  const type = stop.type ? `\nסוג: ${stop.type}` : '';
  const diff = stop.difficulty ? `\nרמת קושי: ${stop.difficulty}` : '';
  const tip = stop.tip ? `\nהערה קיימת מהתכנון: ${stop.tip}` : '';

  // Pull narrative from the matching schedule row (now in Hebrew for translated days).
  let narrative = '';
  for (const d of TRIP.days) {
    const row = (d.schedule || []).find(r => r.stopId === stop.id && r.text);
    if (row) { narrative = row.text; break; }
  }
  const nar = narrative ? `\nתיאור המסלול ליום: ${narrative}` : '';

  return `שם המקום: ${stop.name}
${dayCtx}${region}${hotel}${type}${diff}${coords}${tip}${nar}

הוצאי JSON עם intro, history, geology, lookFor, momTip — בעברית, על המקום הזה. אל תוסיפי טקסט מחוץ ל-JSON.`;
}

// --- candidate stops ---
function candidateStops() {
  const out = [];
  for (const day of TRIP.days) {
    if (ONLY_DAY && day.dayNumber !== ONLY_DAY) continue;
    for (const stop of (day.stops || [])) {
      if (ONLY_STOP && stop.id !== ONLY_STOP) continue;
      if (REDO_STOP && stop.id !== REDO_STOP) continue;
      if (!stop.coordinates) continue;       // skip stops without a place to anchor to
      if (!stop.name) continue;
      if (guides[stop.id] && !REDO_STOP) continue;  // resume — skip already done
      out.push({ day, stop });
    }
  }
  return out;
}

// --- caller ---
async function generateOne(day, stop) {
  const userPrompt = buildUserPrompt(day, stop);
  const resp = await client.messages.create({
    model: MODEL,
    max_tokens: 1500,
    system: SYSTEM,
    messages: [{ role: 'user', content: userPrompt }],
  });
  const txt = resp.content
    .filter(b => b.type === 'text')
    .map(b => b.text)
    .join('')
    .trim();

  // Strip optional ```json fences.
  const cleaned = txt.replace(/^```(?:json)?\s*/i, '').replace(/\s*```\s*$/, '').trim();
  let parsed;
  try { parsed = JSON.parse(cleaned); }
  catch (e) {
    throw new Error(`JSON parse failed for ${stop.id}: ${e.message}\nRaw: ${cleaned.slice(0, 400)}`);
  }
  // Cache stats — Anthropic returns hits as part of usage.
  const u = resp.usage || {};
  const cacheRead = u.cache_read_input_tokens || 0;
  const cacheWrite = u.cache_creation_input_tokens || 0;
  return { guide: parsed, usage: { ...u, cacheRead, cacheWrite } };
}

(async () => {
  const todo = candidateStops().slice(0, LIMIT);
  console.log(`Stops to generate: ${todo.length}`);
  if (!todo.length) { console.log('Nothing to do.'); return; }

  let totalCost = 0;
  for (let i = 0; i < todo.length; i++) {
    const { day, stop } = todo[i];
    const tag = `[${i + 1}/${todo.length}] D${day.dayNumber} ${stop.id} ${stop.name.slice(0, 40)}`;
    process.stdout.write(`${tag} ... `);
    try {
      const { guide, usage } = await generateOne(day, stop);
      guides[stop.id] = { ...guide, _model: MODEL, _at: new Date().toISOString() };
      saveGuides(guides);
      // Sonnet 4.6 pricing: $3/M input, $15/M output, cache reads $0.30/M, writes $3.75/M.
      const costIn  = (usage.input_tokens || 0)        * 3 / 1_000_000;
      const costOut = (usage.output_tokens || 0)       * 15 / 1_000_000;
      const costCR  = (usage.cacheRead || 0)           * 0.30 / 1_000_000;
      const costCW  = (usage.cacheWrite || 0)          * 3.75 / 1_000_000;
      const c = costIn + costOut + costCR + costCW;
      totalCost += c;
      console.log(`ok  in=${usage.input_tokens||0} out=${usage.output_tokens||0} cr=${usage.cacheRead} cw=${usage.cacheWrite}  $${c.toFixed(4)}`);
    } catch (e) {
      console.log(`FAIL ${e.message.slice(0, 200)}`);
    }
  }
  console.log(`\nTotal: ${todo.length} stops, ~$${totalCost.toFixed(2)} run cost.`);
  console.log(`Guides written to: ${GUIDES_PATH}`);
})();
